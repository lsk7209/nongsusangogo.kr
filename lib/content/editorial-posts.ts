export const editorialPosts: EditorialPost[] = drafts.map((draft, index) => {
  const publishAt = addHours("2026-06-06T16:00:00+09:00", index * 5);
  const publishReadyDraft = {
    ...draft,
    body: [...draft.body, buildPracticalClosing(draft, index)],
    deepDives: buildDeepDives(draft, index),
  };

  return {
    ...publishReadyDraft,
    metaTitle:
      publishReadyDraft.title.length > 58
        ? publishReadyDraft.title.slice(0, 58)
        : publishReadyDraft.title,
    metaDescription: `${publishReadyDraft.mainKeyword}, ${publishReadyDraft.expandedKeywords.join(", ")} 기준으로 ${publishReadyDraft.intent} 판단을 돕는 농산물 장보기 가이드입니다.`,
    qualityScore: calculateQualityScore(publishReadyDraft),
    publishAt,
    codexOnly: true,
  };
});

export function getPublishedEditorialPosts(now = new Date()) {
  return editorialPosts.filter((post) => new Date(post.publishAt) <= now);
}

export function getEditorialPost(slug: string, now = new Date()) {
  return getPublishedEditorialPosts(now).find((post) => post.slug === slug);
}

function addHours(iso: string, hours: number) {
  const date = new Date(iso);
  date.setHours(date.getHours() + hours);
  return date.toISOString();
}

function calculateQualityScore(post: PostDraft & Pick<EditorialPost, "deepDives">) {
  const textLength = post.body.join("").length;
  const hasKeywordCoverage =
    post.title.includes(post.mainKeyword) &&
    post.subtitle.includes(post.mainKeyword) &&
    post.expandedKeywords.some((keyword) => post.title.includes(keyword)) &&
    post.expandedKeywords.some((keyword) => post.subtitle.includes(keyword));
  const hasUsefulStructure =
    post.quickAnswer.length >= 80 &&
    post.body.length >= 5 &&
    textLength >= 900 &&
    post.deepDives.length >= 3 &&
    post.deepDives.map((section) => section.body).join("").length >= 650 &&
    post.checklist.length >= 4 &&
    post.faq.length >= 2;
  const hasTrustSignals =
    post.internalLinks.length >= 2 &&
    post.externalSource.href.startsWith("https://") &&
    post.cta.length >= 20;

  if (!hasKeywordCoverage || !hasUsefulStructure || !hasTrustSignals) {
    return 80;
  }

  return Math.min(98, 92 + Math.floor(textLength / 900));
}

